    <p>Alacağınız aracın tescil belgesinde bulunan motor ve şasi numarası ile araç üzerindeki motor ve şasi numarasının aynı olmasına dikkat ediniz. Aracın servis kayıtlarını kontrol edin. İlgilendiğiniz araçla ilgili satıcıdan detaylı bilgi alınız. Araç plaka numarası ile araç üzerinde herhangi bir hak mahrumiyeti (hacizli, rehinli vb.) olup olmadığını araştırın.</p>
    <p>Satıcıyla yüz yüze görüşmeden kesinlikle para göndermeyin. Aracı görmeden kapora için para gönderilmesi taleplerini kabul etmeyin. Alacağınız aracı görmeden, hiçbir sebeple kapora ve benzeri bir ödeme gerçekleştirmeyin veya aracı satın almayın.</p>
    <p>Para ödemesi ile ürün teslimini (araç devir işlemini) aynı anda yapın. Ayrıca aracın ruhsat sahibi kimse parayı da o kişinin hesabına çıkarın. Başka kişiye ait hesaplara para yatırmayın.</p><br>
