		<h1><b>Porsche</b></h1>
		<p>Porsche, Almanlar tarafından üretimi yapılan bir otomobil markasıdır. Marka adını kendisini dünya pazarına kazandıran Alman mühendis Ferdinand Porsche’den alır. Almanlar tarafından üretilmiş en sıra dışı araçlar arasında yer alan Porsche, lüks otomobil sınıfının dünya pazarında yer alan öncüleri arasındadır. Kurulduğu ilk yıllarda sadece yarış otomobilleri tasarlayan ve üreten Porsche, sonraki yıllarda halkın da kullanabileceği spor araçlar üretmeye başlamıştır. Dünyanın gelmiş geçmiş en önemli ticari markalarından biri olarak kabul edilen Porsche, günümüz itibariyle dünyaca ünlü Alman otomotiv devi Volkswagen’in bir ürünü konumundadır. Tıpkı Audi, Seat, Skoda gibi bir Volkswagen Gurup ürünü olan Porsche, dünyanın en çok tercih edilen lüks araç markalarından biridir. Yüksek performanslı araçlar üreten Porsche, günümüzde sedan modellerine sahip olduğu gibi, son yıllarda da SUV alanında çığır açacak bir marka haline gelmiştir. Porsche, dünya çapında bir marka haline gelmiş ve özel bir müşteri kitlesine sahip olmuş vaziyettedir.</p><br>
		<p>Telaffuzu konusunda çoğu insan hataya düşmektedir. Markabir Alman markasıdır ve telaffuzu da aynen şu şekildedir: ”pooğşı”. Ancak Türkler genellikle hatalı bir şekilde ”porş” derler ve Almanları son derece kızdırırlar. Dünya üzerinde en çok tercih edilen ultra lüks araçlar arasında yer alan Porsche, dünyanın hemen hemen her ülkesine ihracat edilmektedir. Resmi olarak 1931 yılında kurulmuş olan Porsche, şirket merkezini tıpkı Mercedes gibi Almanya‘nın önde gelen sanayi şehirlerinden Stuttgart’da bulundurmaktadır. Dünyanın en çok kar eden otomobil markalarından biri olan Porsche, özellikle Avrupa pazarında hatırı sayılır bir satış rakamına sahiptir. Yıllık geliri yaklaşık olarak 22 milyar dolar olan Porsche, 3 milyar dolara yakın kar marjına sahiptir. Aslında her ne kadar Volkswagen Gurubu’na ait olsa da aslına Volkswagen, Porsche’ye aittir desek daha isabetli olacaktır. Bunun nedeni, Porsche markasının yaratıcısının Porsche üretmesine karşın, Alman devleti tarafından halkın büyük bir kesiminin bütçesine uygun bir araba üretme projesinde yer alması sonucu ortaya Volkswagen’in çıkmasıdır. Bu anlamda Volkswagen aslında Porsche önderliğinde ”halkın arabası” adıyla üretilmiştir.</p><br>
		<p>Bu anlamda her iki markada aslında aynı mühendis tarafından tasarlanılan markalardır. Ancak şirket adı Volkswagen olarak belirlenmiştir. Porsche, 2002 yılına dek sadece spor ve yarış otomobilleri üretmekte idi. Ancak sonrasında marka, halkın birçok kesimine hitap edebilecek modeller üretmeye başladı bu da şirketin yıllık gelirinin 15 yıl sonunda 5 katına ulaşmasına imkan sağlamıştır. Yani bu yenilikçi tavır şirketin büyümesine ve de cirosunu 5’e katlamasına sebebiyet vermiştir. Günümüz itibariyle şirket 5 farklı model üretmektedir. Bunlar 911, Boxter Cayman, Panamera, Cayenne ve Macan’dır. Şirketin yıllık satışı yaklaşık olarak 60 bin araçla sınırlıdır.</p><br>
