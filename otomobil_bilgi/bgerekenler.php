    <h1><b><span class="badge badge-secondary ">AZ YAKMANIN ALTIN KURALLARI</span></b></h1>
      </div>
    </div>
    <img src="image/yakit.jpg">  
    <br><br>  
    <p>
    Akaryakıt fiyatları cebinizi yakıyorsa bir gözatın. Yapacağınız birkaç değişiklikle tasarruf yapmak mümkün. Klima kullanımından vites değişimine kadar birçok ayrıntı yakıt harcamanızı doğrudan etkiliyor. Mümkün olan en yüksek vitesi seçin Otomobili belirli bir hızda mümkün olan en yüksek viteste kullanmak yakıt tasarrufunuzu arttırabilir. Düşük viteslerde, yüksek motor devriyle seyretmek yakıt tüketimini artırır. Klima, motor için ekstra bir yüktür. Klima kullanılmaması yüzde 3’e kadar yakıt tasarrufu sağlar. Özellikle kışın dışarıdaki hava sıcaklıkları izin verdiği ölçüde klimayı kapatmayı tercih edin. Gereksiz yüklerden kurtulun: Araç ne kadar ağır olursa hareket etmek için o kadar fazla enerji gerekir. Ekstra her 50 kilogram yük için yakıt tüketiminiz yaklaşık yüzde 1 artar.
    </p>
    <p>Camları kapalı tutun Camların açılması aerodinamik çekiş oluşturur, bu da aracınızı yavaşlatır. Gerekli hızda seyretmek için daha fazla enerji gerekir ve bu da daha fazla yakıta ihtiyaç duyar. Yetersiz şişirilmiş lastikler, yuvarlanmak için daha fazla enerji gerektirir. Janta gevşek bir şekilde monte edilen lastikler zaman içinde hava basıncı kaybı gösterir. Doğru şişirme yüzde 5’e kadar yakıt tasarrufu sağlayabilir.</p>
    <p>Bakımı aksatmayın, kirlenmiş bujiler, kirli hava filtresi veya tıkalı yakıt filtresi yakıt tasarrufunuzu olumsuz bir şekilde etkiler. Bakımı iyi yapılmış bir motor yakıt tasarrufunu yüzde 4’e kadar artırabilir. Araca uygun olmayan ebat ve tipte lastik kullanmak tüketimi ciddi miktarda artırır.</p><br>
