		<h1><b>Ferrari</b></h1>
		<p>Ferrari S.p.A., merkezi ve kuruluş yeri İtalya'nın Modena şehrinin yakınındaki Maranello'da olan İtalyan bir lüks spor otomobili, coupe, süperspor otomobil, spor otomobil üreticisidir. 1929'da Alfa Romeo'nun yarış takımı olarak kurulmuş, 1947'de pist dışına da yasal olarak uygun otomobil üretimine başlamıştır. Ferrari'nin hikâyesi, Enzo Ferrari'nin yaşam hikâyesi olarak kabul edilir. Ferrari üzerine yayınlanan hemen hemen tüm belgeseller ve tanıtımlarda Enzo Ferrari'nin yaşam hikâyesi anlatılır.</p><br>
		<h3><b>Tarihçe</b></h3>
		<p>Enzo Ferrari, Modena şehrinde 1898 yılında dünyaya geldi. I. Dünya Savaşı'nda babasını ve kardeşlerini kaybettikten sonra 1916 yılında, henüz 18 yaşındayken yalnız kaldı. Yarışçılık kariyeri de bu dönemlerde başladı. 1920 yılında Alfa Romeo'nun yarış pilotlarından biri oldu, ismi de bu dönemde bir efsane misali kulaktan kulağa yayıldı. Onun da öncülüğünde Alfa Romeo araba yarışçılığının bir numaralı ismi oldu. Enzo Ferrari'nin hız tutkusu ve daha önce hiç kimsenin ulaşamadığı hızlarda virajları dönebilme kabiliyetinin yanı sıra arabasının ergonomisinden motor aksamına her şeyiyle ilgilenmesi, onu araba yarışları tarihinin ölümsüzleri arasına soktu. 1929 yılında Scuderia Ferrari'yi Alfa Romeo'nun yarış takımı olarak kurdu. 1932'ye kadar yarışmaya devam etti; ancak oğlu Dino Ferrari'nin doğumuyla yarışlardan ayrılarak, otomobil fabrikasının işlerine geçti. Bu dönemde araba motorlarının güçsüzlüğü ve hafif spor arabaların gelişmesi üzerine çalışmalara başladı.</p><br>
		<p>Enzo Ferrari'nin yarış otomobillerini yaratma becerisi onu Alfa Romeo'nun sportif direktörü yaptı. Ancak Enzo Ferrari'nin detaylarına bakmadan imzaladığı direktörlük kontratında, ayrılması durumunda 4 yıl başka bir yarış takımında çalışamayacağı ve tasarım yapmasının yasak olduğu kuralı vardı. Böylece kendi yarattığı, o güne kadar eşi benzeri görülmemiş otomobil ergonomisini geliştirmesine de izin verilmedi. Direktörlükten ayrılmasıyla Auto-Avio Costruzioni'yi kurdu. Bu şirket bazı yarış takımlarına parçalar satıyordu. Doğduğu şehir Modena'da küçük bir araba üretimi atolyesi kurdu ve burada sadece kendinin kullanması için bir araba yapmaya çalıştı. Ancak II. Dünya Savaşı'nın başlamasıyla İtalya zor günler geçirmekteydi. Modena'nın bombalanmasıyla, tasarımına yeni başladığı arabayı da bitiremeden Maranello'ya taşınmak zorunda kaldı. Yıllar sonra Ferrari Modena 360, Enzo Ferrari'nin kütüphanesinde kalan basit bir ön tasarımı baz alarak Pininfarina tarafından yapılacaktır.</p><br>
		<h3><b>Ferrari satış prensipleri</b></h3>
		<p>AuScuderia Ferrari, yakaladığı trendi kullanmasını iyi bilerek talep gördüğü dönemlerde kısıtlı araç üreterek araçlara olan rağbeti artırma yoluna gitmiştir. Bu sayede herkes araca sahip olamıyor ancak bir sonraki modele talep artıyordu. Bu sayede fiyat politikası yükselme eğilimi gösterir oluyordu. Bu teknik genel olarak bütün az üretim yapan otomotiv şirketleri tarafından kullanılmaktadır. Ancak Ferrari'nin buna ek olarak yaptığı diğer bir husus ise, araçlarını ikinci ele düştüğü zaman aracı kurumlar vasıtasıyla değerinin üstünde fiyatlara toplamasıdır. Bu yolla Ferrari marka araçlar değer kaybetmeyen araçlar olarak anılmaya başlanmıştır. Doğal olarak bu da marka imajı için her üreticinin arayıp da bulamadığı bir katkı demektir. Bu sayede değerine değer katan marka hâlen avrupa da ve tüm dünyada elindeki araçları satmak isteyen tüm Ferrari kullanıcılarından aracı alarak bu marka otomobillerin ikinci elde piyasanın serbest ellerine düşmesini engellemektedir. Bu yüzden avrupa kaynaklı oto ikinci el sitelerinde eski model veya düşük fiyatlı Ferrari modeline rastlamak zordur, zira bütün satılan araçlarını Ferrari titiz bir çalışma ile her zaman takip altında tutar.</p><br>
