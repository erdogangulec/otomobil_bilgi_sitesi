		<h1><b>Lamborghini</b></h1>
		<p>Lamborghini, bir İtalyan süperspor otomobil, spor otomobil, SUV markasıdır. Feruccio Lamborghini (1916-1993) tarafından kurulmuştur. Bologna ile Modena arasındaki küçük bir köy olan Sant'Agata Bolognese'da firmanın merkezi ve fabrikası bulunmaktadır. Şirket Audi aracılığıyla Volkswagen AG'ye aittir.</p><br>
		<p>1963 yılında <b>Lamborghini V12 350 GTV</b> modelini üreten firma bu aracı Torino otomobil fuarında otomobil severlerle buluşturdu fakat küçük bir eksiklikle. Aracın motoru aracın içinde değil yanında sergilendi. Motor araca sığmadığı için bu şekilde sergilenirken yaklaşık 20 yıl sonra motor kaputun altına monte edilerek tam bir otomobil olarak sevenleriyle buluştu. Bu işlemin yapılma sebebi ise aracın müzeye koyularak sergilenme isteğidir ve araç şu anda Santa Agata’daki <b>Lamborghini müzesinde</b> bulunmaktadır. 350 GT modelinden sonra Lamborghini 400 GT modelinin üretimini gerçekleştirdi. 2+2 kişilik spor bir otomobil olan 400 GT büyük beğeni topladı. 1965 yılına gelindiğinde Lamborghini 3. modeli olan Miura’yı piyasaya sürdü. Oldukça büyük bir başarı elde eden Lamborghini Miura 761 adet üretildi ve Lamborghini’yi sayılı spor araba üreticileri arasına soktu. Miura’dan sonra 1968 yılında Espada modeli üretidi ve 1978 yılına kadar üretimine devam edildi.</p><br>
		<p>1978 yılına kadar başka model araçların üretimine devam eden Lamborghini aynı yıl <b>iflas</b> açıklamak zorunda kaldı. 1970’lerin başında başlayan ve tüm dünyayı etkisi altına alan petrol krizi Lamborghini’yi de büyük oranda etkiledi. Şirketi öncelikle İsveçli Mimran kardeşler satın almış ardından sırasıyla Chrysler ve Megatech’e satılan Lamborghini son olarak Audi markasının bünyesinde varlığını devam ettirmiştir. 2003 yılına gelindiğinde Lamborghini Audi markası altında ilk üretimini<b> Gallardo</b> modeli ile yaptı. Gallardo markanın en çok satılan modeli olarak tarihe geçti. İlk modelin beğenilmesinin ardından 4 farklı Gallardo modeli daha üreten Lamborghini diğer modellerde de benzer tepkiler alarak tüketicileri tarafından beğeni topladı. Gallardo modellerinden sonra Murcielago, Aventador, Sesto Elemento ve Huracan modellerini piyasaya süren Lamborghini spor ve lüks araba üretimi konusunda sektörünün öncüsü olarak varlığını sürdürmektedir..</p><br>
