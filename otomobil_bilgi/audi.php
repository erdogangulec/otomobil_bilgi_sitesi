		<h1><b>Audi</b></h1>
		<p>Audi, Alman menşeili bir otomobil şirketidir ve Volkswagen grubunun bir markasıdır. Şirketin merkezi Ingolstadt, Bavyera'da bulunmaktadır.Şirketin geçmişi 1899 yılına ve August Horch'a dayanmaktadır. İlk Horch otomobili kendisi tarafından 1901 yılında tasarlanmıştı. 1910 yılında Horsche, şirket dışına atılmış ve kendi adını, eski ortaklarıyla olan anlaşmazlıklar nedeniyle yaptığı tasarımlarda kullanamayacak hale gelmişti. Eski Almancada anlamı "Dinle!" olan "Horch", Latincede aynı anlama gelen Audi'yi kendi markası olarak kullanmaya başladı. 1932 yılında Audi, Auto Union'ı oluşturmak üzere Horch, DKW ve Wanderer şirketleri ile birleşti. Auto Union'ın kullandığı birbirine bağlı dört halka da bugün Audi'nin logosu olarak kullanılmaktadır. II. Dünya Savaşı sonrasında şirket, DKW etrafında ürünlerini sunmaya çalıştı; ancak iki çekişli motoru o kadar ünlü olamadı. Eylül 1965'te Audi, dünyanın en modern motorlarından biriyle tekrar bir çıkış yaparak 72 beygirlik 4 kapılı sedanını piyasaya sundu.</p><br>

		<p>1970'lerde Audi, 1950 yıllarında dünyanın en büyük motosiklet üreten şirket olan NSU ile birleşti. Bu şirket Neckarsulm, Stuttgart yakınında bulunmaktaydı. NSU daha küçük arabalar yapmayı öngörüyordu. Daha sonrasında yeni rotasyon motorları Felix Wankel'in fikirleriyle kullanılmaya başlandı. 1967'de çıkartılan yeni NSU Ro 80 bir uzay çağı arabasıydı ve o gün irtibarıyla sunduğu aerodinamik, ağırlık ve güvenlik donanımları açısından kusursuzdu; ancak motorlardaki rotasyon hatası NSU için pahalıya patlamıştı. Yine de, günümüzde hâlâ Audi'nin bazı modelleri Neckarsulm'da üretilmektedir.</p><br>

		<p>Audi 80 modeli ile beraber, tüm modeller "Quattro" özelliğini kazandı. Audi 80, her ne kadar 1986 yılında "dede arabası" imajına sahip olsa da, 1991 yılında yapılan makyaj çalışmasıyla unutulmaz bir tasarım piyasaya sunuldu. Bu model oldukça iyi satış rakamlarına ulaştı. Gelişen teknoloji ile, Audi dünyanın en gelişmiş motorunu kullanmaya başladı. 1995'te S4 modelinde kullanılan dört çekerli motoru bu döneme damgasını vurdu.</p><br>
		<p>1994'te de dünyanın tamamen alüminyumdan yapılmış ilk seri üretim otomobili olan A8 modelini tanıtan Audi, bu modelinde ASF olarak adlandırılan "Alüminyum uzay kafesi" teknolojisini kullanmaya başladığı ilk araçtır. Şu anda bu teknolojiye sahip bir diğer aracı olan R8, tamamen ASF teknolojisi ile üretilmektedir. Ayrıca ortadan konumlu motor ile otomobil tarihinde binek araçlarda çığır açmıştır. 90'ların ortasında yeni serilerini piyasaya süren Audi, dünyanın en kaliteli otomobilleri arasında yerini aldı. 2000 yılı itibarıyla yarış dünyasının en zorlu ve prestijli yarışlarından olan 24 saatlik Le Mans yarışını 4 kez art arda kazanan Audi, 2003'te de VW grubunun başka bir markası olan Bentley ile tamamen Audi kadrosu altında bu başarıya imza attı.</p><br>
