		<h1>Otomobilin Tarihi</h1>
      </div>
  	</div>
		<p>
		Dodge, Amerika Birleşik Devletleri kökenli otomotiv markası. Horace Dodge ve John Dodge kardeşler tarafından kurulan firma ilk kurulduğunda Ford Model T için parça üretmiştir. Otomobil üretimine başladıktan sonra 1914'ten 1927'ye kadar Dodge Brothers Motor Vehicle Company ismini kullanmıştır. 1928'de Chrysler A.Ş. tarafından satın alınmıştır. Şirket 1970'li yıllarda aile otomobilleri, 1980'li yıllarda ise küçük hacimli ve önden çekişli otomobil üretimine başlamıştır. 1990'lı yıllarda ürün gamına pikap ve ligt-truck modellerini de katmıştır. Chrysler ile birlikte 2002'de DaimlerChrysler Otomotiv Grubu'na katılmıştır. Ancak 2007 yılında DaimlerChrysler Grubunun dağılması sonucu, bağlı bulunduğu ana firma Chrysler Grubunu Cerberus adlı yatırım firması tarafından satın alınmıştır.</p><br>
		<p>Dodge Charger, Dodge tarafından 1966-1978 yılları arasında üretilen muscle car modelidir. Ayrıca 1983-1987 yılları arasında önden çekişli bir hatchback olarak üretilmiş olup Charger adı 2006 yılında yeni bir arkadan itişli dört kapılı sedan olarak dönmüştür.</p><br>
