		<h1><b>Bmw Şirketinin Tarihi</b></h1>
		<p>Bayerische Motoren Werke AG (Türkçe: Bavyera Motor Fabrikaları A.Ş.) genellikle anılan kısaltmasıyla BMW, 1916 yılında kurulan Alman: otomobil, motosiklet, motor ve bisiklet üreticisidir. BMW ayrıca, Mini ve Rolls-Royce, otomobil şirketlerinin sahibidir. Çalışan sayısı 120.726 dur. 2020 cirosu 98,990 Milyar Euro’dur, aynı yıl 2.494.451 araç üretmiştir. Bu üretimin 2.028.841 adedi BMW markası altındadır. Ayrıca aynı yıl motosiklet üretimi 169.272 adettir. Sadece motor sporlarına özel BMW M'i de bünyesinde bulundurur.</p><br>

		<p>Şirketin sloganı ve resmî kurumsal dili İngilizcedir. Sloganı 'Sheer Driving Pleasure' (Gerçek Sürüş Keyfi). Bu sloganla, ünlü Alman teknolojisini ve Ar-Ge'deki kendine güveni vurgulamaktadır.</p><br>

		<p>Şirket, 1913 yılında Karl Friedrich Rapp tarafından Almanya'nın Münih kentinde kurulmuştur ve mimari yönden meşhur merkezi hâlen oradadır. İlk zamanlarda sadece uçak motoru üreten şirket, 1928 yılında satın aldığı Fahrzeugfabrik Eisenach A.G. otomobil şirketinden sonra otomobil üretimine girmiştir. BMW ilk otomobil seri üretimini 1929'da 3/15 PS ismindeki otomobil ile yapmıştır. 1945 yılında 2. Dünya Savaşı'nın sona ermesi ile birlikte müttefik kuvvetler firmanın fabrikalarını kullanılamaz hale getirmiş ve BMW 1948 yılında daha ucuz motosiklet üretimine geçene kadar mutfak ve bahçe malzemeleri üretmiştir. BMW'nin otomobil piyasasına dönüşü 1950'li yıllarda olmuş olsa da düşük satışlar pek iç açıcı olmamış ama firma 1960'lı yıllarda muhtelif spor sedan yapan bir firmaydı. Bu yüzden parçalı amblemin mavi kısmı gökyüzünü beyaz kısmı da uçak pervanesini temsil etmektedir. Başka bir iddia ise amblemdeki mavi beyaz renklerin Bavyera eyaletinin renklerinden geldiğidir.</p><br>
		
