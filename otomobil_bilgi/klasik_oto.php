		<p>
		Yeni GM Stingray'in Mk1 Cobra tarafından kısa bir süreliğine gösterildiği '62'nin sonlarından bu yana (gövde arızası müdahale edene kadar) Grand Sport Corvette programının gelişimi bir hızla devam etti ve 125 otomobillik bir yapı serisine gideceği düşünülüyordu. Bu, GM'nin doğrudan FIA GT yarış sınıfında rekabet etmesine izin verecektir. Sadece bunu birleştirmek için Enzo Ferrari, 250LM'nin homologasyonu talebiyle FIA'da başka bir "hızlı olanı" çekmeye çalışıyordu. FIA, Enzo'nun yıllık minimum 100 garantisinden önce homologasyon verdiği 250GTO'nun ciddi üretim eksikliğini unutmamıştı... Üç yıl içinde birbirinden çok farklı iki şasi ile sadece otuz altı adet üretildi. İkisi de aracın temelini oluşturması gereken 250 GT'ye çok benzemiyordu. Önümüzdeki göreve hazırlanmak amacıyla alternatif motorlar düşünüldü. 289CID yaprak yaylı Cobra, ABD yerel yarış serisinde (USRRC) tamamen baskınken, üç yıllık yarışta sadece bir yarış kaybedilirken, FIA GT sınıfındaki durum farklıydı. Bu, esas olarak, çok daha yüksek sürekli hızlara izin veren devre sayısından kaynaklanıyordu. Burada aerodinamik daha büyük bir el aldı ve roadster'ı dezavantajlı duruma getirdi. Bu, AC, Shelby ve Willment ekibinin darbelerini inşa etmesinden daha az olmayan bir şekilde dengelendi.
</p><br>
