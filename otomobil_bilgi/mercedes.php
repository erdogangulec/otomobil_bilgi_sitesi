		<h1><b>Mercedes-Benz</b></h1>
		<p>Mercedes-Benz, 1926 yılında Karl Benz'in şirketi Benz & Cie. ve Gottlieb Daimler'in şirketi Daimler Motoren Gesellschaft'in birleşmesi sonucu kurulmuş otomotiv ve motor markası. Almanya'nın Stuttgart şehrinde kurulmuştur.</p><br>
		<h3><b>İsminin Kaynağı</b></h3>

		<p>1897 yılında Fransa'nın Nice kentinde yaşayan Avusturyalı tüccar ve Avusturya Nice Başkonsolosu Emil Jellinek, Daimler fabrikasını ziyaret ederek bir otomobil satın aldı. Uluslararası finans dünyası ve aristokrasi ile iyi ilişkiler içinde olan Jellinek, Daimler otomobili ile Fransız Riviera'sında büyük ilgi topladı. Daha sonra Jelinek 1899'da 23 beygir gücünde motorla donatılmış bir Daimler yarış otomobiline büyük kızı Mercedes'in adını vererek bu araçla Nice'de bir yarışa katıldı ve birinci oldu. Bu başarıdan sonra Jelinek, Daimler fabrikasına 36 otomobil sipariş verdi ve bu araçların "Mercedes" adını taşımalarını şart koştu.</p><br>

		<p>Emil Jelinek'in elde ettiği satış başarısı üzerine Daimler, 1901 yılından itibaren ürettiği araçları "Mercedes" olarak adlandırmaya karar verdi. Mercedes İspanyolca konuşulan ülkelerde çok kullanılan bir isimdir. Kelime olarak da Mars gezegeninin İspanyolca adıdır. Lütuf ve zarafet anlamına da gelmektedir. 23 Haziran 1902 tarihinde Mercedes marka adı olarak tescil edildi. 26 Eylül 1902 tarihinden itibaren de kanunlarca koruma altına alındı.</p><br>
		<h3><b>Amblem</b></h3>
		<p>Şirketin kurucusu Karl Benz, Deutz'daki motor fabrikasındaki görevinin ilk yıllarında, Köln ve Deutz manzaralı evinin tepesine bir yıldız amblemi koymuş, eşine yazdığı mektuplarda bu yıldızın günün birinde başarıyı ve gücü temsil edeceğini ve fabrikasının üzerinde parlayacağını söylemişti. Yıldız Daimler'in, motorlu araçların "karada, suda, havada" evrenselliğini simgelemektedir. 1909'da tescil edildi. 1916 yılında yıldızın etrafı, içinde dört küçük yıldızın ve Mercedes isminin yer aldığı bir daireye çevrildi. 1926'da Daimler-Benz birleşmesi ile Benz'in defne yapraklarından çelengi yıldızın etrafını sardı.</p><br>
		
